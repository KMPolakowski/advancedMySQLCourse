use sample_staff;

DROP FUNCTION IF EXISTS CALC_DISTANCE;
DELIMITER //

CREATE FUNCTION CALC_DISTANCE (
	point1 POINT,
    point2 POINT
) RETURNS DOUBLE
BEGIN
	RETURN ST_Distance_Sphere(point1, point2);
END;


//

DELIMITER ;

SELECT CALC_DISTANCE(POINT(20.509193, 50.416448), POINT(16.377291, 48.203686));

